FROM SB1990 SB1 
INNER JOIN SA5990 SA5
ON SB1.B1_COD = SA5.A5_PRODUTO AND SB1.D_E_L_E_T_ = SA5.D_E_L_E_T_
INNER JOIN SA2990 SA2
ON SA5.A5_FORNECE = SA2.A2_COD AND SA2.D_E_L_E_T_ = SA5.D_E_L_E_T_
WHERE SA5.D_E_L_E_T_ = ''