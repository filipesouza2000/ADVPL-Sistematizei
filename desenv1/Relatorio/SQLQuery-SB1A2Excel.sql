SELECT C7_FORNECE,A2_COD, A2_NOME, C7_NUM,C7_EMISSAO,C7_PRODUTO,C7_QUANT
FROM		SC7990 C7 
INNER JOIN	SA2990 A2
ON	C7.C7_FILIAL = A2.A2_FILIAL
AND C7.C7_LOJA   = A2.A2_LOJA
AND C7.C7_FORNECE= A2.A2_COD
AND C7.D_E_L_E_T_ = A2.D_E_L_E_T_
WHERE A2.D_E_L_E_T_ = ''

