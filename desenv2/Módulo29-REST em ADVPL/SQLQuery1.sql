ON		B1.D_E_L_E_T_ = BM.D_E_L_E_T_
AND		B1.B1_GRUPO = BM.BM_GRUPO
WHERE B1.D_E_L_E_T_ = '' AND B1.B1_COD LIKE 'WS%'

